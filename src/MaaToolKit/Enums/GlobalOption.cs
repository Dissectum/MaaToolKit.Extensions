namespace MaaToolKit.Enums;

/// <summary>
///     Global options
/// </summary>
public enum GlobalOption
{
    /// <summary>
    ///     Invalid option
    /// </summary>
    Invalid = 0,

    /// <summary>
    ///     The logging option, value is the path to the log directory
    /// </summary>
    Logging = 1,

    /// <summary>
    ///     The debug mode option, value is bool indicating whether turns on the debug mode.
    /// </summary>
    DebugMode = 2,
}
